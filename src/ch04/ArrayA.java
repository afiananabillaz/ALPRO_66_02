package ch04;

public class ArrayA {
    public static void main(String[] args) {
        int [] nilai= {82,12,41,38,19,26,9,48,20,55,8,32,3};
        System.out.print("Menampilkan semua nilai di dalam array = ");
        for(int x=0; x< nilai.length; x++){
            System.out.print(nilai[x]+" ");
        }
    }
}
